OBTER_DADOS_MATERIAL(CD_MATERIAL,'GRU')DS_GRUPO_MATERIAL,
OBTER_DADOS_MATERIAL(CD_MATERIAL,'CSUB')CD_SUBGRUPO_MATERIAL,
OBTER_DADOS_MATERIAL(CD_MATERIAL,'SUB')DS_SUBGRUPO_MATERIAL,
OBTER_DADOS_MATERIAL(CD_MATERIAL,'CCLA')CD_CLASSE_MATERIAL,
