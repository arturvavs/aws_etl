SELECT 
NR_MOVIMENTO_ESTOQUE,
CD_LOCAL_ESTOQUE,
OBTER_DESC_LOCAL_ESTOQUE(CD_LOCAL_ESTOQUE)DS_ESTOQUE,
DT_MOVIMENTO_ESTOQUE,
CD_OPERACAO_ESTOQUE,
OBTER_DESC_OPERACAO_ESTOQUE(CD_OPERACAO_ESTOQUE)DS_OPERACAO,
CD_ACAO,
CD_MATERIAL,
OBTER_DESC_MATERIAL(CD_MATERIAL)DS_MATERIAL,
